package com.example.lesson_7;

public class Fooddd {
    int photo;
    String title;
    String desc;
    int price;

    public Fooddd(int photo,String title,String desc,int price){
        this.photo=photo;
        this.title=title;
        this.desc=desc;
        this.price=price;

    }

    public int getPhoto() {
        return photo;
    }

    public String getTitle() {
        return title;
    }

    public String getDesc() {
        return desc;
    }

    public int getPrice() {
        return price;
    }
}